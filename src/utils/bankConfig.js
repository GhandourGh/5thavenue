// Get bank by code
export const getBankByCode = code => {
  return COLOMBIAN_BANKS.find(bank => bank.code === code);
};

// Get bank by name
export const getBankByName = name => {
  return COLOMBIAN_BANKS.find(bank =>
    bank.name.toLowerCase().includes(name.toLowerCase())
  );
};

// Search banks by name
export const searchBanks = query => {
  if (!query) return COLOMBIAN_BANKS;

  return COLOMBIAN_BANKS.filter(bank =>
    bank.name.toLowerCase().includes(query.toLowerCase())
  );
};
